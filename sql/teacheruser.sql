SELECT
'2021' AS 'SCHOOLYEAR',
'T' AS 'ROLE',
TCH.ID AS 'LASID',
'' AS 'SASID',
TCH.TF AS 'FIRSTNAME',
'' AS 'MIDDLENAME',
TCH.TLN AS 'LASTNAME',
[GRADE] = CASE TCH.SC
WHEN 30 THEN '9-12'
WHEN 31 THEN '9-12'
WHEN 32 THEN '9-12'
WHEN 10 THEN '6-8'
WHEN 15 THEN '6-8'
WHEN 20 THEN '6-8'
WHEN 21 THEN '6-8'
END,
TCH.EM AS 'USERNAME',
'' AS 'PASSWORD',
'MDR' AS 'ORGANIZATIONTYPEID',
[ORGANIZATIONID] = CASE TCH.SC
WHEN 10 THEN '00127361'
WHEN 15 THEN '01876802'
WHEN 20 THEN '01400348'
WHEN 21 THEN '01877105'
WHEN 30 THEN '00127282'
WHEN 31 THEN '00127309'
WHEN 32 THEN '05346122'
END,
TCH.EM AS 'PRIMARYEMAIL',
'ED' AS 'HMHAPPLICATIONS'
FROM TCH
WHERE TCH.DEL = 0
AND TCH.TG = ''
AND TCH.SC = 20 and TCH.TN in (9,22,28,19,26,6,143)
OR TCH.SC = 21 and TCH.TN IN (15,11,26,77,30,18,31)
OR TCH.SC = 10 AND TCH.TN IN (4,8,27)
OR TCH.SC = 15 AND TCH.TN IN (2,42,11,73)
OR TCH.SC = 30 AND TCH.TN = 65
OR TCH.SC = 31 AND TCH.TN IN (16,37,81,71)
OR TCH.SC = 32 AND TCH.TN IN (189,45,55,30,60)
