OR SEC.DEL = 0 AND STU.TG = '' AND STU.DEL = 0 AND SEC.SC = 15 AND SEC.SE IN (22,34,43,3,23,46,44,45,100)
OR SEC.DEL = 0 AND STU.TG = '' AND STU.DEL = 0 AND STU.SC = 30 AND SEC.SE IN (402,301,203,403,302,204)
OR SEC.DEL = 0 AND STU.TG = '' AND STU.DEL = 0 AND STU.SC = 31 AND SEC.SE IN (570,1144,48,47,210,632,49,806,325)
