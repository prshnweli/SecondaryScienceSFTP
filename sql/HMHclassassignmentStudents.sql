'2020' AS 'SCHOOLYEAR',
CAST(SEC.SE AS VARCHAR) + '_' + CAST(SEC.SC AS VARCHAR) AS 'CLASSLOCALID',
STU.ID AS 'LASID',
'S' AS 'ROLE',
