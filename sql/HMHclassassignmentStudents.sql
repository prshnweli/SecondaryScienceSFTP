SELECT
'2021' AS 'SCHOOLYEAR',
CAST(SEC.SE AS VARCHAR) + '_' + CAST(SEC.SC AS VARCHAR) AS 'CLASSLOCALID',
STU.ID AS 'LASID',
'S' AS 'ROLE',
'' AS 'POSITION'
FROM SEC
JOIN STU ON (SEC.SC = STU.SC) AND (SEC.SN = STU.SN)
WHERE SEC.DEL = 0 AND STU.TG = '' AND STU.DEL = 0 AND STU.SC = 20 AND SEC.SE IN (400,106,617,401,107,402,526,413,317,403,630,415,528,632,140,414,527,533,631,412,223,525)
OR SEC.DEL = 0 AND STU.TG = '' AND STU.DEL = 0 AND STU.SC = 21 AND SEC.SE IN (20,21,23,22,117,216,318,515,616,110,209,312,415,609,115,215,421,513,614,26,27)
OR SEC.DEL = 0 AND STU.TG = '' AND STU.DEL = 0 AND STU.SC = 10 AND SEC.SE IN (230,238,425,233,348,623,419,229,438)
OR SEC.DEL = 0 AND STU.TG = '' AND STU.DEL = 0 AND SEC.SC = 15 AND SEC.SE IN (22,34,43,3,23,46,44,45,100)
OR SEC.DEL = 0 AND STU.TG = '' AND STU.DEL = 0 AND STU.SC = 30 AND SEC.SE IN (102,103,302,303,404,405)
OR SEC.DEL = 0 AND STU.TG = '' AND STU.DEL = 0 AND STU.SC = 31 AND SEC.SE IN (546,52,53,1144,570,48,47,49)
OR SEC.DEL = 0 AND STU.TG = '' AND STU.DEL = 0 AND STU.SC = 32 AND SEC.SE IN (106,107,206,207,305,404,505,506,605,606,105,204,312,410,510)
ORDER BY STU.SC, SEC.SE
