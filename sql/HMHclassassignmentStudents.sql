FROM SEC
JOIN STU ON (SEC.SC = STU.SC) AND (SEC.SN = STU.SN)
WHERE SEC.DEL = 0 AND STU.TG = '' AND STU.DEL = 0 AND STU.SC = 20 AND SEC.SE IN (430,622,429,621,434,620,338,241,623,247,343,446,248,344,447,635,242,240,337,443,126)
